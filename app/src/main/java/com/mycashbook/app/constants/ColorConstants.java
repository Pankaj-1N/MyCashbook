    // ============================================================
    // UTILITY METHOD: Get color based on transaction type
    // ============================================================
    /**
     * Get color for transaction type
     * @param transactionType "DEBIT" or "CREDIT"
     * @return Color int
     */
    public static int getTransactionColor(String transactionType) {
        if (transactionType != null) {
            if ("DEBIT".equalsIgnoreCase(transactionType)) {
                return COLOR_TRANSACTION_OUT;
            } else if ("CREDIT".equalsIgnoreCase(transactionType)) {
                return COLOR_TRANSACTION_IN;
            }
        }
        return COLOR_TEXT_SECONDARY;
    }

    /**
     * Get color for balance status
     * @param balance Balance amount
     * @return Color int
     */
    public static int getBalanceColor(double balance) {
        if (balance > 0) {
            return COLOR_TRANSACTION_IN;      // Green
        } else if (balance < 0) {
            return COLOR_TRANSACTION_OUT;     // Red
        } else {
            return COLOR_TEXT_SECONDARY;      // Grey
        }
    }

    /**
     * Get color for status
     * @param status "success", "error", "warning", "info"
     * @return Color int
     */
    public static int getStatusColor(String status) {
        if (status != null) {
            switch (status.toLowerCase()) {
                case "success":
                    return COLOR_SUCCESS;
                case "error":
                    return COLOR_ERROR;
                case "warning":
                    return COLOR_WARNING;
                case "info":
                    return COLOR_INFO;
                default:
                    return COLOR_TEXT_SECONDARY;
            }
        }
        return COLOR_TEXT_SECONDARY;
    }
